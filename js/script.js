$(document).ready(function() {
    $(function() {
    $(".preload").fadeOut(1000, function() {
        $(".content").fadeIn(500); 
        $(".WdgMoyklassModalItem-dialog").fadeIn(500); 
    $(function(){
		$('a[href^="#"]').click(function(){
			var target = $(this).attr('href');
			$('html, body').animate({scrollTop: $(target).offset().top}, 1000);
			return false; 
		}); 
});
});
});
});

